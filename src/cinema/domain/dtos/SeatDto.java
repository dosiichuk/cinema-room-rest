package cinema.domain.dtos;

public class SeatDto {
    private int row;
    private int column;

    public SeatDto() {
    }

    public SeatDto(int row, int column) {
        this.row = row;
        this.column = column;
    }

    public int getRow() {
        return row;
    }

    public int getColumn() {
        return column;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public void setColumn(int column) {
        this.column = column;
    }
}
